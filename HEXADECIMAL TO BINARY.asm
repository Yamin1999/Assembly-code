;CONVERT HEXADECIMAL TO BINARY


INCLUDE "EMU8086.INC"
.MODEL SMALL
.STACK 100H
.DATA

.CODE

MAIN PROC
    
    
MOV AX,@DATA     ;GET DATA SEGMENT
MOV DS,AX        ;INITIALIZE DS 

MOV CL,4         ;COUNTERT FOR 4 SHIFTS
XOR BX,BX        ;CLEAR BX

PRINT 'ENTER A HEX NUM:  '
WHILE:
MOV AH,1         ;INPUT CHARECTER FUNCTION
INT 21H          ;INPUT A CHARECTER
CMP AL,0DH       ;CARRY RETURN
JE END_WHILE     ;YES! EXIT

;CONVERT CHARECTER TO BINARY VALUE

CMP AL,58        
JG LETTER        ;A LETTER
JLE NUMBER       ;A DIGIT


NUMBER:

AND AL,0FH       ;CONVERT DIGIT TO BINARY VALUE
JMP SHIFT        ;GO TO INSERT IN BX


LETTER:
SUB AL,37H       ;CONVERT LETTER TO BINARY VALUE
JMP SHIFT

SHIFT:

SHL BX,CL        ;MAKE ROOM FOR NEW VALUE 

;INSERT VALUE INTO BX
OR BL,AL         
JMP WHILE

END_WHILE:

PRINTN
PRINT 'EQUIVALENT BINARY NUM:  '

MOV CX,16        ;MOVE VALUE IN CX FOR LOOPING

LOOP1:
SHL BX,1
JC CARRY         ;IF CARRY

PRINT '0'        ;IF NOT CARRY
JMP ENDK

CARRY:
PRINT '1'

ENDK:
LOOP LOOP1       ;END LOOP

;DOS EXIT
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN


 
   
    
    